import 'leaflet/dist/leaflet.js';
import './leaflet.shpfile.js'
import 'leaflet.markercluster/dist/leaflet.markercluster.js'


class Map {

    constructor(args = {}) {
        this.window = args.window;
        this.location = args.location;
        this.selector = args.selector;
        this.world = null;
        this.shapes = [];
        this.markers = [];
        this.agencies = [];
        this.token = process.env.VUE_APP_MAP_TOKEN;
        this.mapConfig = {
            defaultZoom: 5,
            url: `https://api.tiles.mapbox.com/v4/{id}/{z}/{x}/{y}.png?access_token=${this.token}`,
            layerConfig: {
                attribution: 'Map data &copy; <a href="https://www.openstreetmap.org/">OpenStreetMap</a> contributors, <a href="https://creativecommons.org/licenses/by-sa/2.0/">CC-BY-SA</a>, Imagery Â© <a href="https://www.mapbox.com/">Mapbox</a>',
                maxZoom: 18,
                id: 'mapbox.streets',
                accessToken: this.token,
                blur: 40
            }
        }
        this.postInit();
    }

    postInit() {
        this.world = this.window.L.map(this.selector, {
            center: this.location,
            zoom: this.mapConfig.defaultZoom
        });

        this.window.L.tileLayer(
            this.mapConfig.url,
            this.mapConfig.layerConfig
        ).addTo(this.world);

        this.markers = this.window.L.markerClusterGroup({
            // maxClusterRadius: 20,
            // disableClusteringAtZoom: 14,
            removeOutsideVisibleBounds: true,
        });

        this.window.L.control.scale().addTo(this.world);
    }

    setShapefile(shfile, args = {}) {
        let options = {
            'weight': 1,
            'opacity': 0.65
        }
        options = Object.assign(options, args);
        this.shapes.push(new this.window.L.Shapefile(shfile, options).addTo(this.world));
        return this;
    }

    setLocation(location) {
        this.location = location;
        this.world.setView(this.location); 
        return this;
    }

    setPositions() {
        const first = [positions[0].lat, positions[0].long];
        const last = [positions[positions.length-1].lat, positions[positions.length-1].long];
        this.window.L.marker(first, this.icon('bus')).addTo(this.world);
        this.window.L.marker(last, this.icon('bus')).addTo(this.world);
        const coords = positions.map((latlng) => [
            latlng.lat,
            latlng.long,
        ]);
        this.window.L.polyline(coords, {color: 'orange'}).addTo(this.world);
        return this;
    }

    setHeatLayer(layer) {
        layer.forEach((latlng) => {
            let marker = this.window.L.marker(latlng, this.icon('stop'));
            this.markers.addLayer(marker);
        });
        this.world.addLayer(this.markers);
        return this;
    }

    setAgenciesLayer(layer) {
        layer.forEach((latlng) => {
            let marker = this.window.L.marker(
                [latlng.lat, latlng.long], this.icon('agency')
            )
            marker.addTo(this.world);
            this.agencies.push(marker);
        });
        return this;
    }

    setBounds(bounds) {
        this.window.L.rectangle(
            bounds,
            {color: 'red', weight: 2, fill: false}
        ).addTo(this.world);
        return this;
    }

    clear(typ) {
        switch(typ) {
            case 'shapes':
                this.shapes.forEach(e => this.world.removeLayer(e));
                break;
            case 'agencies':
                this.agencies.forEach(e => this.world.removeLayer(e));
                break;
        }
        return this;
    }

    icon(icn) {
        let url = '';
        let size = [24, 24];
        if (icn === 'bus') {
            url = 'https://img.icons8.com/plasticine/100/000000/bus.png'; 
            size = [40, 40]
        } else if (icn == 'stop') {
            url = 'https://img.icons8.com/office/24/000000/stop-sign.png';
        } else if (icn == 'agency') {
            url = 'https://img.icons8.com/plasticine/100/000000/apartment.png';
            size = [12, 12];
        }
        const icon = this.window.L.icon({
            iconUrl: url,
            iconSize:     size, // size of the icon
        });
        return {icon};
    }
}

const MapFactory = {
    build: (args) => {
        return new Map(args);
    }
}

